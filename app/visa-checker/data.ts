// ─── Quiz types ────────────────────────────────────────────────────────────────
export type DenialValue = '' | 'never' | 'once_old' | 'once_recent' | 'multiple'
export type FinancialValue = '' | 'strong' | 'good' | 'moderate' | 'limited'

export type QuizAnswers = {
  passport: string
  destination: string
  denial: DenialValue
  ties: string[]
  financial: FinancialValue
}

export const EMPTY_ANSWERS: QuizAnswers = {
  passport: '', destination: '', denial: '', ties: [], financial: '',
}

// ─── Score logic ──────────────────────────────────────────────────────────────
export function calculateScore(a: QuizAnswers): number {
  let score = STRONG_PASSPORTS.has(a.passport) ? 85 : WEAK_PASSPORTS.has(a.passport) ? 55 : 50
  if (a.denial === 'never')         score += 15
  else if (a.denial === 'once_old')    score -= 10
  else if (a.denial === 'once_recent') score -= 20
  else if (a.denial === 'multiple')    score -= 30
  const noTies = a.ties.length === 0 || a.ties.includes('none')
  score += noTies ? -15 : Math.min(a.ties.filter(t => t !== 'none').length * 5, 25)
  if (a.financial === 'strong')   score += 10
  else if (a.financial === 'good')   score += 5
  else if (a.financial === 'limited') score -= 10
  return Math.max(5, Math.min(95, score))
}

export type ScoreInfo = { label: string; color: string; stroke: string; textColor: string; badgeBg: string }

export function getScoreInfo(score: number): ScoreInfo {
  if (score >= 80) return { label:'STRONG',     color:'#10b981', stroke:'#10b981', textColor:'text-emerald-600', badgeBg:'bg-emerald-100 text-emerald-700' }
  if (score >= 60) return { label:'GOOD',        color:'#f59e0b', stroke:'#f59e0b', textColor:'text-yellow-600',  badgeBg:'bg-yellow-100 text-yellow-700'  }
  if (score >= 40) return { label:'MODERATE',    color:'#f97316', stroke:'#f97316', textColor:'text-orange-600',  badgeBg:'bg-orange-100 text-orange-700'  }
  return             { label:'CHALLENGING', color:'#ef4444', stroke:'#ef4444', textColor:'text-red-600',    badgeBg:'bg-red-100 text-red-700'        }
}

export function getPassportStrengthLabel(passport: string): string {
  if (STRONG_PASSPORTS.has(passport)) return 'Strong'
  if (WEAK_PASSPORTS.has(passport))   return 'Weak'
  return 'Moderate'
}

export function getRecommendations(a: QuizAnswers) {
  const recs: { icon: string; text: string; href?: string }[] = []
  const noTies = a.ties.length === 0 || a.ties.includes('none')
  if (noTies)
    recs.push({ icon:'📋', text:'Gather proof of property, employment or family ties to strengthen your application' })
  if (a.financial === 'moderate' || a.financial === 'limited')
    recs.push({ icon:'💰', text:'Provide 6 months of bank statements showing consistent income and savings' })
  if (a.denial === 'once_recent' || a.denial === 'multiple')
    recs.push({ icon:'📝', text:'Write a strong cover letter explaining the circumstances of your previous denial' })
  else if (a.denial === 'once_old')
    recs.push({ icon:'📝', text:'Address your past denial proactively — briefly explain what has changed since then' })
  recs.push({ icon:'🎤', text:'Practice common visa interview questions to boost your confidence', href:'/interview-prep' })
  if (!noTies && a.ties.length < 3)
    recs.push({ icon:'🏠', text:'Document all ties to home: property deeds, employment letters, family records' })
  return recs.slice(0, 5)
}

export function isLikelyVisaFree(passport: string, destination: string): boolean {
  return STRONG_PASSPORTS.has(passport) && STRONG_PASSPORTS.has(destination)
}
